public class IncrementAndDecrement {
    public static void main(String[]args)
    {
    //Increment operator
    int a = 34;
    a++;
    int b = a;
    int c = ++a;
        System.out.println(b);
        System.out.println(c);
    //Decrement operator
    int x = 43;
    x--;
    int y = x;
    int z = --x;
        System.out.println(y);
        System.out.println(z);

    int p = 7;
    int q = p++*8;
        System.out.println(q);


    }
}
